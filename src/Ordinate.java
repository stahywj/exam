public class Ordinate {
    private double row_x;
    private double row_y;
    private double col_x;
    private double col_y;

    public void setRow_x(double row_x) {
        this.row_x = row_x;
    }

    public double getRow_x() {
        return row_x;
    }

    public void setRow_y(double row_y) {
        this.row_y = row_y;
    }

    public double getRow_y() {
        return row_y;
    }

    public void setCol_x(double col_x) {
        this.col_x = col_x;
    }

    public double getCol_x() {
        return col_x;
    }

    public void setCol_y(double col_y) {
        this.col_y = col_y;
    }

    public double getCol_y() {
        return col_y;
    }
}
